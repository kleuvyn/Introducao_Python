# Dia 6: Escreva um programa que calcule a média de três notas fornecidas pelo usuário.

nota_1 = float(input('Digite a primeira nota: '))
nota_2 = float(input('Digite a segunda nota: '))
nota_3 = float(input('Digite a terceita nota: '))
nota_4 = float(input('Digite a quarta nota: '))
soma = (nota_1 + nota_2 + nota_3 + nota_4)/4

print(f'\n A sua media final é {soma}')