'''
    Declaração de Variáveis:
        As variáveis são declaradas atribuindo um valor a um nome.
        Não é necessário especificar o tipo de variável ao declará-la; o tipo é inferido a partir do valor atribuído.
        Os nomes de variáveis podem começar com uma letra (a-z, A-Z) ou um sublinhado (_) e podem conter letras, números e sublinhados.
'''
idade = 25
nome = "Maria"
salario = 1500.50

'''    
    Tipos de Dados:
        Em python suporta vários tipos de dados, incluindo inteiros, ponto flutuante, string, booleano, listas, tuplas, dicionários, conjuntos, etc.
        O tipo de uma variável pode mudar dinamicamente conforme o programa é executado.
'''
numero = 10        # Inteiro
peso = 63.5        # Ponto flutuante
nome = "João"      # String
esta_chovendo = True   # Booleano
lista = [1, 2, 3]  # Lista

'''
    Atribuição de Variáveis:
        As variáveis podem ser atribuídas a novos valores a qualquer momento.
        Uma variável pode ser atribuída a outra variável.
'''
x = 5
y = x      # y agora tem o mesmo valor que x (5)
x = 10     # x agora tem um novo valor (10), mas y ainda é 5

'''
    Nomes de Variáveis Convenções:
        Geralmente, em Python, os nomes de variáveis são escritos em minúsculas.
        Para nomes compostos, é comum usar sublinhados para separar palavras (snake_case).
'''
nome_completo = "Ana Silva"
idade_usuario = 30
